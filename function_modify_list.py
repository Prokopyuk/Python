"""
По факту собираем новый список прямо в хвосте предыдущего, добавляя в конец 
списка чётные числа, поделенные на 2, а все исходные элементы списка - удаляем.

for x in l[:] - перебор элементов ведём по копии списка, полученной с 
помощью среза, т.к. исходный список мы меняем прямо во время прохода,
 а в этом случае простой поэлементный перебор "ломается".
"""
lst = [10, 5, 8, 3]


def modify_list(l):
    for i in l[:]:
        if i % 2 == 0:
            l.append(i // 2)
        l.remove(i)


modify_list(lst)
print(lst)

